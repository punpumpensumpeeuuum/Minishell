#include "libft.h"

int	ft_isprint(int c, int flag)
{
	if (flag == 1)
	{
		if (c >= 32 && c <= 126)
			return (1);
		return (0);
	}
	if (c >= 33 && c <= 126)
		return (1);
	return (0);
}
